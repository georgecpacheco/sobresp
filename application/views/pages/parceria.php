        <h1>Parceria</h1>
        <p>Para a oferta de seus cursos de pós-graduação, a SOBRESP
            mantém uma parceria com a Universidade Cruzeiro do Sul,
            de São Paulo, reconhecida pela experiência e tradição na gestão de cursos <i>lato sensu</i>.</p>
        <p>A UNICSUL/SP é uma das maiores instituições de ensino privado do Brasil, tendo mais de 30.000
            alunos matriculados nos cursos oferecidos em seus quatro campi, além de dezenas de unidades
            avançadas espalhadas pelo Brasil.</p>
        <p>A parceria entre a SOBRESP e a UNICSUL/SP, portanto, é mais uma ação que acrescenta experiência
            e garantia de qualidade na formação de nossos alunos.</p>
        <p>Para mais informações acesse o site <a href="http://www.cruzeirodosul.edu.br" target="_blank">http://www.cruzeirodosul.edu.br</a></p>
